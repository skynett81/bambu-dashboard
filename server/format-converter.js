/**
 * Format Converter — bidirectional STL / OBJ / 3MF conversion.
 *
 * All converters use the indexed-mesh form
 *   { positions: Float32Array(3·V), indices: Uint32Array(3·F) }
 * so they pair naturally with mesh-repair.js and mesh-transforms.js.
 *
 * 3MF support is delegated to lib3mf via the existing `mesh-builder.js`
 * helper (writer) and `lib3mf-parser.js` (reader). STL is implemented
 * natively (binary write, ASCII fallback). OBJ is plain-text only.
 */

import { parseStl } from './stl-analyzer.js';
import { parse3mfBuffer } from './lib3mf-parser.js';
import { getLib } from './mesh-builder.js';

// ── Format detection ──────────────────────────────────────────────────

/**
 * Sniff a buffer to figure out which format it is. Detects by extension
 * hint (if provided) and falls back to magic bytes.
 *
 * @param {Buffer | Uint8Array} buf
 * @param {string} [filename]
 * @returns {'stl' | 'obj' | '3mf' | 'unknown'}
 */
export function detectFormat(buf, filename) {
  const u8 = buf instanceof Uint8Array ? buf : new Uint8Array(buf);
  if (filename) {
    const lower = String(filename).toLowerCase();
    if (lower.endsWith('.stl')) return 'stl';
    if (lower.endsWith('.obj')) return 'obj';
    if (lower.endsWith('.3mf')) return '3mf';
  }
  // 3MF is a ZIP container — magic 'PK'
  if (u8[0] === 0x50 && u8[1] === 0x4b) return '3mf';
  // ASCII STL begins with 'solid'
  if (u8.length >= 5) {
    const head = String.fromCharCode(...u8.slice(0, 5)).toLowerCase();
    if (head === 'solid') return 'stl';
  }
  // Binary STL: 80-byte header + uint32 triangle count, total length matches
  if (u8.length >= 84) {
    const dv = new DataView(u8.buffer, u8.byteOffset, u8.byteLength);
    const tri = dv.getUint32(80, true);
    if (84 + tri * 50 === u8.length) return 'stl';
  }
  // OBJ: heuristic — sample contains 'v ' or 'f '
  if (u8.length >= 16) {
    const sample = new TextDecoder('utf-8', { fatal: false }).decode(u8.slice(0, Math.min(2048, u8.length)));
    if (/^[ \t]*v[ \t]+-?[\d.]/m.test(sample) || /^[ \t]*f[ \t]+\d/m.test(sample)) return 'obj';
  }
  return 'unknown';
}

// ── STL: import ───────────────────────────────────────────────────────

/**
 * Convert a parsed STL triangle list (produced by stl-analyzer.parseStl)
 * into our indexed mesh form. Performs basic vertex deduplication so the
 * resulting indices are usable for repair/transforms.
 */
export function stlBufferToMesh(buf) {
  const parsed = parseStl(buf);
  // parseStl returns { triangles: Float32Array, count, format } where each
  // triangle is 12 floats (nx, ny, nz, v1, v2, v3 each xyz).
  const tris = parsed.triangles;
  const triCount = parsed.count;
  const positions = [];
  const indices = [];
  const map = new Map();
  const EPS = 1e-5;
  for (let f = 0; f < triCount; f++) {
    const base = f * 12 + 3; // skip 3 normal floats
    for (let k = 0; k < 3; k++) {
      const x = tris[base + 3 * k];
      const y = tris[base + 3 * k + 1];
      const z = tris[base + 3 * k + 2];
      const key = `${Math.round(x / EPS)},${Math.round(y / EPS)},${Math.round(z / EPS)}`;
      let idx = map.get(key);
      if (idx === undefined) {
        idx = positions.length / 3;
        map.set(key, idx);
        positions.push(x, y, z);
      }
      indices.push(idx);
    }
  }
  return {
    positions: new Float32Array(positions),
    indices: new Uint32Array(indices)
  };
}

// ── STL: export (binary) ──────────────────────────────────────────────

/**
 * Serialize an indexed mesh to a binary STL buffer.
 *
 * @param {{positions: Float32Array, indices: Uint32Array}} mesh
 * @returns {Buffer}
 */
export function meshToStlBuffer(mesh) {
  const triCount = mesh.indices.length / 3;
  const headerSize = 80;
  const triSize = 50; // 12 (normal) + 36 (3 vertices) + 2 (attr)
  const out = Buffer.alloc(headerSize + 4 + triCount * triSize);
  // Header
  out.write('Generated by 3DPrintForge mesh toolkit', 0, 'utf-8');
  out.writeUInt32LE(triCount, headerSize);

  let p = headerSize + 4;
  for (let f = 0; f < triCount; f++) {
    const a = mesh.indices[3 * f];
    const b = mesh.indices[3 * f + 1];
    const c = mesh.indices[3 * f + 2];
    const ax = mesh.positions[3 * a],     ay = mesh.positions[3 * a + 1], az = mesh.positions[3 * a + 2];
    const bx = mesh.positions[3 * b],     by = mesh.positions[3 * b + 1], bz = mesh.positions[3 * b + 2];
    const cx = mesh.positions[3 * c],     cy = mesh.positions[3 * c + 1], cz = mesh.positions[3 * c + 2];
    // Compute normal from CCW winding.
    const e1x = bx - ax, e1y = by - ay, e1z = bz - az;
    const e2x = cx - ax, e2y = cy - ay, e2z = cz - az;
    let nx = e1y * e2z - e1z * e2y;
    let ny = e1z * e2x - e1x * e2z;
    let nz = e1x * e2y - e1y * e2x;
    const len = Math.hypot(nx, ny, nz);
    if (len > 0) { nx /= len; ny /= len; nz /= len; }
    out.writeFloatLE(nx, p); out.writeFloatLE(ny, p + 4); out.writeFloatLE(nz, p + 8);
    out.writeFloatLE(ax, p + 12); out.writeFloatLE(ay, p + 16); out.writeFloatLE(az, p + 20);
    out.writeFloatLE(bx, p + 24); out.writeFloatLE(by, p + 28); out.writeFloatLE(bz, p + 32);
    out.writeFloatLE(cx, p + 36); out.writeFloatLE(cy, p + 40); out.writeFloatLE(cz, p + 44);
    out.writeUInt16LE(0, p + 48);
    p += 50;
  }
  return out;
}

// ── OBJ: import ───────────────────────────────────────────────────────

/**
 * Parse a Wavefront OBJ file. Only positions and triangle/quad faces are
 * read — UVs, normals, materials, and groups are ignored. Quads are split
 * into two triangles. Negative (relative) face indices are supported.
 *
 * @param {Buffer | Uint8Array | string} buf
 * @returns {{positions: Float32Array, indices: Uint32Array}}
 */
export function objBufferToMesh(buf) {
  const text = typeof buf === 'string'
    ? buf
    : new TextDecoder('utf-8', { fatal: false }).decode(buf instanceof Uint8Array ? buf : new Uint8Array(buf));
  const positions = [];
  const indices = [];

  const lines = text.split(/\r?\n/);
  for (const raw of lines) {
    const line = raw.trim();
    if (!line || line.startsWith('#')) continue;
    if (line.startsWith('v ')) {
      const parts = line.split(/\s+/);
      // OBJ v lines: v x y z [w] [r g b] — we only take first 3.
      const x = Number(parts[1]);
      const y = Number(parts[2]);
      const z = Number(parts[3]);
      if (Number.isFinite(x) && Number.isFinite(y) && Number.isFinite(z)) positions.push(x, y, z);
    } else if (line.startsWith('f ')) {
      const parts = line.split(/\s+/).slice(1);
      // Each part may be "v", "v/vt", "v//vn", or "v/vt/vn".
      const verts = parts.map(p => {
        const idx = parseInt(p.split('/')[0], 10);
        if (!Number.isFinite(idx)) return -1;
        // OBJ indices are 1-based, possibly negative (relative).
        const totalV = positions.length / 3;
        return idx > 0 ? idx - 1 : totalV + idx;
      }).filter(i => i >= 0);
      // Triangulate fan-style if a polygon.
      for (let k = 1; k + 1 < verts.length; k++) {
        indices.push(verts[0], verts[k], verts[k + 1]);
      }
    }
  }
  return {
    positions: new Float32Array(positions),
    indices: new Uint32Array(indices)
  };
}

// ── OBJ: export ───────────────────────────────────────────────────────

/**
 * Serialize an indexed mesh to a Wavefront OBJ string. Includes only
 * positions and faces (no normals, no UVs).
 *
 * @param {{positions: Float32Array, indices: Uint32Array}} mesh
 * @returns {string}
 */
export function meshToObjString(mesh) {
  const lines = ['# Generated by 3DPrintForge mesh toolkit', 'o mesh'];
  for (let i = 0; i < mesh.positions.length; i += 3) {
    lines.push(`v ${mesh.positions[i].toFixed(6)} ${mesh.positions[i + 1].toFixed(6)} ${mesh.positions[i + 2].toFixed(6)}`);
  }
  for (let f = 0; f < mesh.indices.length; f += 3) {
    lines.push(`f ${mesh.indices[f] + 1} ${mesh.indices[f + 1] + 1} ${mesh.indices[f + 2] + 1}`);
  }
  return lines.join('\n') + '\n';
}

// ── 3MF: import ───────────────────────────────────────────────────────

/**
 * Read a 3MF buffer and concatenate every object's mesh into a single
 * indexed mesh. For multi-object files the per-object split is lost —
 * use `splitComponents` afterward if you need it back.
 *
 * @param {Buffer} buf
 */
export async function tmfBufferToMesh(buf) {
  const parsed = await parse3mfBuffer(buf);
  const positions = [];
  const indices = [];
  let offset = 0;
  if (parsed?.objects) {
    for (const obj of parsed.objects) {
      if (!obj.vertices || !obj.triangles) continue;
      for (const v of obj.vertices) positions.push(v[0], v[1], v[2]);
      for (const tri of obj.triangles) indices.push(offset + tri[0], offset + tri[1], offset + tri[2]);
      offset = positions.length / 3;
    }
  }
  return {
    positions: new Float32Array(positions),
    indices: new Uint32Array(indices)
  };
}

// ── 3MF: export ───────────────────────────────────────────────────────

/**
 * Write an indexed mesh as a 3MF file (single component, single object).
 * Uses lib3mf via the shared mesh-builder singleton.
 *
 * @param {{positions: Float32Array, indices: Uint32Array}} mesh
 * @returns {Promise<Buffer>}
 */
export async function meshTo3mfBuffer(mesh) {
  const lib = await getLib();
  const wrapper = new lib.CWrapper();
  const model = wrapper.CreateModel();
  try {
    const meshObj = model.AddMeshObject();
    meshObj.SetName('mesh');
    // Vertices.
    for (let i = 0; i < mesh.positions.length; i += 3) {
      const p = new lib.sPosition();
      p.set_Coordinates0(mesh.positions[i]);
      p.set_Coordinates1(mesh.positions[i + 1]);
      p.set_Coordinates2(mesh.positions[i + 2]);
      meshObj.AddVertex(p);
      p.delete();
    }
    // Triangles.
    for (let f = 0; f < mesh.indices.length; f += 3) {
      const t = new lib.sTriangle();
      t.set_Indices0(mesh.indices[f]);
      t.set_Indices1(mesh.indices[f + 1]);
      t.set_Indices2(mesh.indices[f + 2]);
      meshObj.AddTriangle(t);
      t.delete();
    }
    // Build item: ensure the object appears in the scene.
    const id = new lib.sTransform();
    id.set_Fields_0_0(1); id.set_Fields_1_1(1); id.set_Fields_2_2(1);
    model.AddBuildItem(meshObj, id);
    id.delete();

    const writer = model.QueryWriter('3mf');
    const vfsPath = `/export_${Date.now()}.3mf`;
    writer.WriteToFile(vfsPath);
    const data = lib.FS.readFile(vfsPath);
    try { lib.FS.unlink(vfsPath); } catch { /* ignore */ }
    writer.delete();
    return Buffer.from(data);
  } finally {
    try { model.delete(); } catch { /* ignore */ }
    try { wrapper.delete(); } catch { /* ignore */ }
  }
}

// ── High-level convert ────────────────────────────────────────────────

/**
 * Convert a buffer between supported formats. The output format is
 * inferred from `targetFormat` ('stl' | 'obj' | '3mf').
 *
 * Returns `{ format, buffer, mesh }` so callers that already have a mesh
 * can chain repairs/transforms on the parsed copy.
 */
export async function convertBuffer(buf, targetFormat, sourceFilename) {
  const source = detectFormat(buf, sourceFilename);
  if (source === 'unknown') throw new Error('Unable to detect source format');

  let mesh;
  if (source === 'stl') mesh = stlBufferToMesh(buf);
  else if (source === 'obj') mesh = objBufferToMesh(buf);
  else if (source === '3mf') mesh = await tmfBufferToMesh(buf);

  let outBuf;
  const fmt = String(targetFormat || '').toLowerCase();
  if (fmt === 'stl') outBuf = meshToStlBuffer(mesh);
  else if (fmt === 'obj') outBuf = Buffer.from(meshToObjString(mesh), 'utf-8');
  else if (fmt === '3mf') outBuf = await meshTo3mfBuffer(mesh);
  else throw new Error(`Unsupported target format: ${targetFormat}`);

  return { format: fmt, buffer: outBuf, mesh, sourceFormat: source };
}

/**
 * Serialize a mesh directly (no parsing) to the chosen format.
 */
export async function meshToBuffer(mesh, format) {
  const fmt = String(format || '').toLowerCase();
  if (fmt === 'stl') return meshToStlBuffer(mesh);
  if (fmt === 'obj') return Buffer.from(meshToObjString(mesh), 'utf-8');
  if (fmt === '3mf') return await meshTo3mfBuffer(mesh);
  throw new Error(`Unsupported format: ${format}`);
}

/**
 * Parse any supported buffer into a mesh.
 */
export async function bufferToMesh(buf, filename) {
  const fmt = detectFormat(buf, filename);
  if (fmt === 'stl') return stlBufferToMesh(buf);
  if (fmt === 'obj') return objBufferToMesh(buf);
  if (fmt === '3mf') return await tmfBufferToMesh(buf);
  throw new Error('Unable to detect input format');
}
